function changeColor() {
  $('#quote').fadeOut();
  $('#title').fadeOut();
  //Choose random color
  var mycolor = bgarr[Math.floor(Math.random()*bgarr.length)];
  //animate color **jquery color needed
  console.log(mycolor);
  $("html").animate({
    backgroundColor:mycolor.bgColor,
  }, 1000);
  $(".bg-quote").animate({
    color:mycolor.textclr
  }, 1000);
  $(".info").animate({
    color:mycolor.textclr
  }, 1000);


  //ajax api call
  $.ajax( {
    url: 'http://quotesondesign.com/wp-json/posts?filter[orderby]=rand&filter[posts_per_page]=1',
    beforeSend: function(){
      $(  "#space" ).toggleClass( "space-bar" );
    },
    success: function(data) {
      $(  "#space" ).toggleClass( "space-bar" );
      $('#quote').html($(data[0].content).text()).fadeIn();
      $('#title').html("- " + data[0].title).fadeIn();
    },
    cache: false
  });
}

//modal trigger
function displayModal() {
  $('#modal1').toggle();
}

//keypress events
$('body').keyup(function(e){
  if(e.keyCode === 32){
    changeColor();
  }
  if(e.keyCode === 73){
    displayModal();
  }
});
